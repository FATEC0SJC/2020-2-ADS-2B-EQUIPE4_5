    public void preencherveiculos(){
    
    conex.conexao();
    conex.executasql("select *from base_veiculo order by modelo");   
        try {
            conex.rs.first();
            jComboBoxVeiculo.removeAllItems();
            do{
                jComboBoxVeiculo.addItem(conex.rs.getString("modelo"));
                }while(conex.rs.next());
        } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane,"erro ao preencher bairros");
            
        }
    
    
    conex.desconecta();
    }
    public void preencherfuncionario(){
    
    conex.conexao();
    conex.executasql("select *from funcionarios order by nome_funcionario");   
        try {
            conex.rs.first();
            jComboBoxfuncionarios.removeAllItems();
            do{
                jComboBoxfuncionarios.addItem(conex.rs.getString("nome_funcionario"));
                }while(conex.rs.next());
        } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane,"erro ao preencher bairros");
            
        }
    
    
    conex.desconecta();
    }
   

