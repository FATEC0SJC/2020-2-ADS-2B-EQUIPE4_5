    public void preenchertabela(String sql){
        ArrayList dados = new ArrayList();
        String[] colunas = new String []{
        "ID","Nome","Senha","CPF","CNH","Tipo"};
        conex.conexao();
        
        conex.executasql(sql);
        try {
            conex.rs.first();
            do{
            dados.add(new Object[]{conex.rs.getInt("cod_usuario"),conex.rs.getString("nome_funcionario"),conex.rs.getString("senha"),conex.rs.getInt("cpf"),conex.rs.getString("cnh"),conex.rs.getString("tipo_funcionario")});
            
             }while(conex.rs.next());
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"erro ao prenchher");
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTableFuncionario.setModel(modelo);
        
        jTableFuncionario.getColumnModel().getColumn(0).setPreferredWidth(30);
        jTableFuncionario.getColumnModel().getColumn(0).setResizable(false);
           
        jTableFuncionario.getColumnModel().getColumn(1).setPreferredWidth(150);
        jTableFuncionario.getColumnModel().getColumn(1).setResizable(false);
           
        jTableFuncionario.getColumnModel().getColumn(2).setPreferredWidth(120);
        jTableFuncionario.getColumnModel().getColumn(2).setResizable(false);
           
        jTableFuncionario.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTableFuncionario.getColumnModel().getColumn(3).setResizable(false);
           
        jTableFuncionario.getColumnModel().getColumn(4).setPreferredWidth(50);
        jTableFuncionario.getColumnModel().getColumn(4).setResizable(false);
           
        jTableFuncionario.getColumnModel().getColumn(5).setPreferredWidth(25);
        jTableFuncionario.getColumnModel().getColumn(5).setResizable(false);
        
        jTableFuncionario.getColumnModel().getColumn(5).setPreferredWidth(100);
        jTableFuncionario.getColumnModel().getColumn(5).setResizable(false);
           
        
        jTableFuncionario.getTableHeader().setReorderingAllowed(false);
        jTableFuncionario.setAutoResizeMode(jTableFuncionario.AUTO_RESIZE_OFF);
        jTableFuncionario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        conex.desconecta();
    
    }
    
    
