    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
      if(jTextFieldNome.getText().isEmpty()){
      JOptionPane.showMessageDialog(null,"Prencha o campo");
      jTextFieldNome.requestFocus();
      }else if(jTextFieldCpf.getText().isEmpty()){jTextFieldCpf.requestFocus();
      }else{
    
        
        if(flag == 1 ){
      
        
      mod.setNome(jTextFieldNome.getText());
      mod.setCpf(Integer.parseInt(jTextFieldCpf.getText()));
      mod.setSenha(jTextFieldSenha.getText());
      mod.setCnh((String) COMBOBOXCNH.getSelectedItem());
      mod.setTipofuncionario((String) jComboBoxFuncionario.getSelectedItem());
      control.Salvar(mod);
      
      jTextFieldNome.setText("");
      jTextFieldCpf.setText("");
      jTextFieldSenha.setText("");
      
      jTableFuncionario.setEnabled(false);
        jTextFieldCpf.setEnabled(false);
        jTextFieldSenha.setEnabled(false);
        COMBOBOXCNH.setEnabled(false);
        jComboBoxFuncionario.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        jTextFieldNome.setEnabled(false);
        }else{
         mod.setCodigo(Integer.parseInt(jtID.getText()));
         mod.setNome(jTextFieldNome.getText());
         mod.setCpf(Integer.parseInt(jTextFieldCpf.getText()));
         mod.setSenha(jTextFieldSenha.getText());
         mod.setTipofuncionario((String) jComboBoxFuncionario.getSelectedItem());
         mod.setCnh((String) COMBOBOXCNH.getSelectedItem());
         control.Editar(mod);
      }
         jTableFuncionario.setEnabled(false);
        jTextFieldCpf.setEnabled(false);
        jTextFieldSenha.setEnabled(false);
        COMBOBOXCNH.setEnabled(false);
        jComboBoxFuncionario.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        jTextFieldNome.setEnabled(false);
        
        jButtonNovo.setEnabled(true);
        jButtonCancelar.setEnabled(false);
          preenchertabela("select *from funcionarios order by nome_funcionario");
      }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

