package modelo;

public class veiculo {
    private String id_vei;
    private String marca_vei;
    private String modelo_vei;
    private String ano_vei;
    private String cor_vei;
    private String placa_vei;
    private String estado_vei;

    public String getId_vei() {
        return id_vei;
    }

    public void setId_vei(String id_vei) {
        this.id_vei = id_vei;
    }

    public String getMarca_vei() {
        return marca_vei;
    }

    public void setMarca_vei(String marca_vei) {
        this.marca_vei = marca_vei;
    }

    public String getModelo_vei() {
        return modelo_vei;
    }

    public void setModelo_vei(String modelo_vei) {
        this.modelo_vei = modelo_vei;
    }

    public String getAno_vei() {
        return ano_vei;
    }

    public void setAno_vei(String ano_vei) {
        this.ano_vei = ano_vei;
    }

    public String getCor_vei() {
        return cor_vei;
    }

    public void setCor_vei(String cor_vei) {
        this.cor_vei = cor_vei;
    }

    public String getPlaca_vei() {
        return placa_vei;
    }

    public void setPlaca_vei(String placa_vei) {
        this.placa_vei = placa_vei;
    }

    public String getEstado_vei() {
        return estado_vei;
    }

    public void setEstado_vei(String estado_vei) {
        this.estado_vei = estado_vei;
    }
    
}
